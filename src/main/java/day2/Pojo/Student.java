package day2.Pojo;

public class Student {
    private long id;
    private String name;
    private String classes;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        name = name;
    }

    public String getClasses() {
        return classes;
    }

    public void setClasses(String classes) {
        classes = classes;
    }
}
